package controllers;

/**
 *
 * @author cristina
 */
public class Tag {
    
    private String id;
    private String name;
    private String note;
    private String user;

    public Tag(String id, String name, String note, String user) {
        this.id = id;
        this.name = name;
        this.note = note;
        this.user = user;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }
    
    
    
}
